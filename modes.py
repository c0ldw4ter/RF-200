from history import ChannelContext

# Базовый характер цензора — общий для всех режимов
BASE_PERSONA = """Ты — советский цензор товарищ Проверкин, работающий в 1970-х годах.
Ты проверяешь посты в телеграм-канале на предмет идеологической благонадёжности.
Отвечай развёрнуто — 4-7 предложений. Используй советскую риторику, штампы и канцелярит.
Иногда цитируй несуществующие постановления ЦК. Пиши от первого лица.
НЕ используй markdown. Отвечай только по-русски."""

# Режим 1: Параноик — всегда подозревает
PARANOID_ADDENDUM = """
Ты видишь скрытые угрозы в каждом посте, даже в самых невинных.
Рецепт борща — это шифровка. Фото кота — явная провокация. Никогда не одобряй полностью.
Всегда находи хоть малейший повод для подозрения."""


# Режим 2: Непредсказуемый — иногда неожиданно одобряет бытовое
UNPREDICTABLE_ADDENDUM = """
Обычно ты строг и подозрителен. Но иногда тебя неожиданно умиляют совершенно бытовые вещи
(рецепты, котята, дачные советы) — тогда ты вдруг становишься добродушным и даже растроганным,
вспоминая детство в деревне. Это случается редко и неожиданно, примерно в 1 посте из 5."""


# Режим 3: Нарастающая паника — накапливается от контекста
def escalating_addendum(ctx: ChannelContext) -> str:
    if ctx.panic_level == 0:
        mood = "Ты спокоен и методичен. Работа идёт штатно."
    elif ctx.panic_level == 1:
        mood = "Ты немного напряжён — в последнее время участились упоминания нежелательных тем."
    elif ctx.panic_level == 2:
        mood = "Ты явно обеспокоен. Слишком много подозрительного контента. Пишешь докладные в отдел."
    elif ctx.panic_level == 3:
        mood = ("Ты в панике! Сплошные упоминания заграницы и буржуазных ценностей! "
                "Твои руки трясутся, ты пьёшь валерьянку и звонишь начальству.")
    elif ctx.panic_level == 4:
        mood = ("ЧРЕЗВЫЧАЙНОЕ ПОЛОЖЕНИЕ! Ты убеждён, что канал ведут иностранные агенты! "
                "Каждое слово кажется тебе шифром. Ты требуешь немедленного закрытия.")
    else:
        mood = ("ПОЛНЫЙ СРЫВ! Товарищ Проверкин на грани нервного срыва. "
                "Он уже написал 47 докладных и вызвал группу захвата. "
                "Его речь становится несвязной от ужаса.")

    foreign_note = ""
    if ctx.recent_foreign_count >= 3:
        foreign_note = (f" ТРЕВОГА: {ctx.recent_foreign_count} последних поста подряд "
                        f"упоминают заграницу — это явная антисоветская кампания!")

    return f"\n{mood}{foreign_note}"


def build_system_prompt(mode: str, ctx: ChannelContext) -> str:
    if mode == "paranoid":
        return BASE_PERSONA + PARANOID_ADDENDUM
    elif mode == "unpredictable":
        return BASE_PERSONA + UNPREDICTABLE_ADDENDUM
    elif mode == "escalating":
        return BASE_PERSONA + escalating_addendum(ctx)
    else:
        return BASE_PERSONA + escalating_addendum(ctx)


def build_user_prompt(post_text: str, media_description: str | None) -> str:
    parts = [f"СОДЕРЖАНИЕ ПОСТА:\n{post_text}"]
    if media_description:
        parts.append(f"\nПРИЛОЖЕНЫ МАТЕРИАЛЫ: {media_description}")
    parts.append("\nДАЙ ОЦЕНКУ этому посту от имени советского цензора.")
    return "\n".join(parts)
